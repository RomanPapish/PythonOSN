#Напишіть програму на Python для додавання двох об’єктів, якщо обидва об’єкти є цілочисельним типом.  
a = input('Введіть перше число= ')
b = input('Введіть друге число= ')
if type(a)==int and type(b)==int:
    s = a + b
    print('Сума чисел= %s' % s)
else:
    print('Помилка: одне з чисел не є цілочисельним!!!')
